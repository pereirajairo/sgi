package br.com.sgi.bean;

/**
 *
 * @author jairo.silva
 */
public class CameraParametro {


    private String urlCamera = "";
    private String usuarioCamera = "";
    private String senhaCamera ="";

    public String getUrlCamera() {
        return urlCamera;
    }

    public void setUrlCamera(String urlCamera) {
        this.urlCamera = urlCamera;
    }

    public String getUsuarioCamera() {
        return usuarioCamera;
    }

    public void setUsuarioCamera(String usuarioCamera) {
        this.usuarioCamera = usuarioCamera;
    }

    public String getSenhaCamera() {
        return senhaCamera;
    }

    public void setSenhaCamera(String senhaCamera) {
        this.senhaCamera = senhaCamera;
    }
 
    

}
